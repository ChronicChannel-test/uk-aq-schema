declare
  v_day_start_utc timestamptz;
  v_day_end_utc timestamptz;
  v_hourly_deleted bigint := 0;
  v_daily_deleted bigint := 0;
begin
  set local timezone = 'UTC';

  if auth.role() <> 'service_role' then
    raise exception 'service_role required';
