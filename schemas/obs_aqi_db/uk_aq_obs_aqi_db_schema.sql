create or replace function uk_aq_ops.uk_aq_db_size_metric_sample_local(
  p_retention_days integer default 120,
  p_recorded_at timestamptz default now(),
  p_source text default 'uk_aq_db_size_logger_pg_cron'
)
returns table (
  rows_upserted int,
  rows_deleted bigint
)
language plpgsql
security definer
set search_path = uk_aq_ops, public, pg_catalog
as $$
declare
  v_days integer;
  v_bucket_hour timestamptz;
  v_rows_upserted int := 0;
  v_rows_deleted bigint := 0;
  v_source text;
  v_oldest_observs timestamptz := null;
  v_oldest_aqilevels timestamptz := null;
  v_oldest_observed_at timestamptz := null;
begin
  v_days := greatest(1, least(coalesce(p_retention_days, 120), 3650));
  v_bucket_hour := date_trunc('hour', coalesce(p_recorded_at, now()));
  v_source := coalesce(nullif(btrim(p_source), ''), 'uk_aq_db_size_logger_pg_cron');

  if to_regclass('uk_aq_observs.observations') is not null then
    execute 'select min(o.observed_at) from uk_aq_observs.observations o'
      into v_oldest_observs;
  end if;

  if to_regclass('uk_aq_aqilevels.timeseries_aqi_hourly') is not null then
    execute 'select min(a.timestamp_hour_utc) from uk_aq_aqilevels.timeseries_aqi_hourly a'
      into v_oldest_aqilevels;
  end if;

  select min(v)
    into v_oldest_observed_at
  from (values (v_oldest_observs), (v_oldest_aqilevels)) as oldest(v);

  insert into uk_aq_ops.db_size_metrics_hourly (
    bucket_hour,
    database_label,
    database_name,
    size_bytes,
    oldest_observed_at,
    source,
    recorded_at,
    updated_at
  )
  values (
    v_bucket_hour,
    'obs_aqidb',
    current_database()::text,
    pg_database_size(current_database())::bigint,
    v_oldest_observed_at,
    v_source,
    coalesce(p_recorded_at, now()),
    now()
  )
  on conflict (bucket_hour, database_label) do update set
    database_name = excluded.database_name,
    size_bytes = excluded.size_bytes,
    oldest_observed_at = excluded.oldest_observed_at,
    source = excluded.source,
    recorded_at = excluded.recorded_at,
    updated_at = now();

  get diagnostics v_rows_upserted = row_count;

  delete from uk_aq_ops.db_size_metrics_hourly
  where bucket_hour < now() - make_interval(days => v_days);

  get diagnostics v_rows_deleted = row_count;

  return query select v_rows_upserted, v_rows_deleted;
end;
$$;

create or replace function uk_aq_ops.uk_aq_schema_size_metric_sample_local(
  p_retention_days integer default 120,
  p_recorded_at timestamptz default now(),
  p_source text default 'uk_aq_schema_size_metrics_pg_cron'
)
returns table (
  rows_upserted int,
  rows_deleted bigint
)
language plpgsql
security definer
set search_path = uk_aq_ops, public, pg_catalog
as $$
declare
  v_days integer;
  v_bucket_hour timestamptz;
  v_rows_upserted int := 0;
  v_rows_deleted bigint := 0;
  v_rows int := 0;
  v_source text;
  v_schema text;
  v_size_bytes bigint := 0;
  v_oldest_observed_at timestamptz := null;
begin
  set local timezone = 'UTC';
  -- Guard against short inherited role/session statement timeouts during
  -- relation-size scans across the obs_aqidb schemas.
  set local statement_timeout = '15min';

  v_days := greatest(1, least(coalesce(p_retention_days, 120), 3650));
  v_bucket_hour := date_trunc('hour', coalesce(p_recorded_at, now()));
  v_source := coalesce(nullif(btrim(p_source), ''), 'uk_aq_schema_size_metrics_pg_cron');

  foreach v_schema in array array['uk_aq_observs', 'uk_aq_aqilevels'] loop
    v_size_bytes := 0;
    v_oldest_observed_at := null;

    if to_regnamespace(v_schema) is not null then
      execute format(
        $sql$
          select coalesce(sum(pg_total_relation_size(c.oid)), 0)::bigint
          from pg_class c
          join pg_namespace n on n.oid = c.relnamespace
          where n.nspname = %L
            and c.relkind in ('r', 'p', 'm', 't')
        $sql$,
        v_schema
      ) into v_size_bytes;

      if v_schema = 'uk_aq_observs'
         and to_regclass('uk_aq_observs.observations') is not null then
        execute 'select min(o.observed_at) from uk_aq_observs.observations o'
          into v_oldest_observed_at;
      elsif v_schema = 'uk_aq_aqilevels'
            and to_regclass('uk_aq_aqilevels.timeseries_aqi_hourly') is not null then
        execute 'select min(a.timestamp_hour_utc) from uk_aq_aqilevels.timeseries_aqi_hourly a'
          into v_oldest_observed_at;
      end if;
    end if;

    insert into uk_aq_ops.schema_size_metrics_hourly (
      bucket_hour,
      database_label,
      schema_name,
      size_bytes,
      oldest_observed_at,
      source,
      recorded_at,
      updated_at
    )
    values (
      v_bucket_hour,
      'obs_aqidb',
      v_schema,
      coalesce(v_size_bytes, 0),
      v_oldest_observed_at,
      v_source,
      coalesce(p_recorded_at, now()),
      now()
    )
    on conflict (bucket_hour, database_label, schema_name) do update set
      size_bytes = excluded.size_bytes,
      oldest_observed_at = excluded.oldest_observed_at,
      source = excluded.source,
      recorded_at = excluded.recorded_at,
      updated_at = now();

    get diagnostics v_rows = row_count;
    v_rows_upserted := v_rows_upserted + coalesce(v_rows, 0);
  end loop;

  delete from uk_aq_ops.schema_size_metrics_hourly
  where bucket_hour < now() - make_interval(days => v_days);

  get diagnostics v_rows_deleted = row_count;

  return query select v_rows_upserted, v_rows_deleted;
end;
$$;

create or replace function uk_aq_ops.uk_aq_obs_aqidb_day_counts_refresh_current(
  p_recorded_at timestamptz default now(),
  p_source text default 'uk_aq_obs_aqidb_day_counts_pg_cron'
)
returns table (
  rows_upserted int,
  rows_deleted bigint
)
language plpgsql
security definer
set search_path = uk_aq_ops, public, pg_catalog
as $$
declare
  v_bucket_hour timestamptz;
  v_rows_upserted int := 0;
  v_rows_deleted bigint := 0;
  v_source text;
begin
  set local timezone = 'UTC';
  set local statement_timeout = '15min';

  v_bucket_hour := date_trunc('hour', coalesce(p_recorded_at, now()));
  v_source := coalesce(nullif(btrim(p_source), ''), 'uk_aq_obs_aqidb_day_counts_pg_cron');

  with observs_counts as (
    select
      (o.observed_at at time zone 'UTC')::date as day_utc,
      count(*)::bigint as row_count
    from uk_aq_observs.observations o
    group by 1
  ),
  observs_days as (
    select
      generate_series(min(c.day_utc), max(c.day_utc), interval '1 day')::date as day_utc
    from observs_counts c
  ),
  observs_rows as (
    select
      'observs'::text as dataset,
      d.day_utc,
      coalesce(c.row_count, 0)::bigint as row_count
    from observs_days d
    left join observs_counts c using (day_utc)
  ),
  aqilevels_counts as (
    select
      (h.timestamp_hour_utc at time zone 'UTC')::date as day_utc,
      count(*)::bigint as row_count
    from uk_aq_aqilevels.timeseries_aqi_hourly h
    group by 1
  ),
  aqilevels_days as (
    select
      generate_series(min(c.day_utc), max(c.day_utc), interval '1 day')::date as day_utc
    from aqilevels_counts c
  ),
  aqilevels_rows as (
    select
      'aqilevels'::text as dataset,
      d.day_utc,
      coalesce(c.row_count, 0)::bigint as row_count
    from aqilevels_days d
    left join aqilevels_counts c using (day_utc)
  ),
  source_rows as (
    select * from observs_rows
    union all
    select * from aqilevels_rows
  )
  insert into uk_aq_ops.obs_aqidb_day_counts_current (
    dataset,
    day_utc,
    row_count,
    bucket_hour,
    source,
    recorded_at,
    updated_at
  )
  select
    s.dataset,
    s.day_utc,
    s.row_count,
    v_bucket_hour,
    v_source,
    coalesce(p_recorded_at, now()),
    now()
  from source_rows s
  on conflict (dataset, day_utc) do update set
    row_count = excluded.row_count,
    bucket_hour = excluded.bucket_hour,
    source = excluded.source,
    recorded_at = excluded.recorded_at,
    updated_at = now();

  get diagnostics v_rows_upserted = row_count;

  with observs_counts as (
    select
      (o.observed_at at time zone 'UTC')::date as day_utc,
      count(*)::bigint as row_count
    from uk_aq_observs.observations o
    group by 1
  ),
  observs_days as (
    select
      generate_series(min(c.day_utc), max(c.day_utc), interval '1 day')::date as day_utc
    from observs_counts c
  ),
  observs_rows as (
    select
      'observs'::text as dataset,
      d.day_utc
    from observs_days d
  ),
  aqilevels_counts as (
    select
      (h.timestamp_hour_utc at time zone 'UTC')::date as day_utc,
      count(*)::bigint as row_count
    from uk_aq_aqilevels.timeseries_aqi_hourly h
    group by 1
  ),
  aqilevels_days as (
    select
      generate_series(min(c.day_utc), max(c.day_utc), interval '1 day')::date as day_utc
    from aqilevels_counts c
  ),
  aqilevels_rows as (
    select
      'aqilevels'::text as dataset,
      d.day_utc
    from aqilevels_days d
  ),
  source_rows as (
    select * from observs_rows
    union all
    select * from aqilevels_rows
  )
  delete from uk_aq_ops.obs_aqidb_day_counts_current t
  where not exists (
    select 1
    from source_rows s
    where s.dataset = t.dataset
      and s.day_utc = t.day_utc
  );

  get diagnostics v_rows_deleted = row_count;

  return query select v_rows_upserted, v_rows_deleted;
end;
$$;

