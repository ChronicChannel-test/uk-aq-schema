-- DB size logger RPC (used by ops db-size logger Cloud Run).
create or replace function uk_aq_public.uk_aq_rpc_database_size_bytes()
returns table (
  database_name text,
  size_bytes bigint,
  sampled_at timestamptz
)
language plpgsql
security definer
set search_path = pg_catalog, public
as $$
begin
  if auth.role() <> 'service_role' then
    raise exception 'service_role required';
  end if;

  return query
  select
    current_database()::text as database_name,
    pg_database_size(current_database())::bigint as size_bytes,
    now() as sampled_at;
end;
$$;

revoke all on function uk_aq_public.uk_aq_rpc_database_size_bytes() from public;
grant execute on function uk_aq_public.uk_aq_rpc_database_size_bytes() to service_role;

-- Metadata RPCs for stations_daily mirror verification/sync.
create or replace function uk_aq_public.uk_aq_rpc_info_schema_columns(
  p_schema text default 'uk_aq_core',
  p_table_names text[] default null
)
returns table (
  table_name text,
  column_name text,
  udt_name text,
  is_nullable text,
  column_default text,
  ordinal_position integer
)
language plpgsql
security definer
set search_path = pg_catalog, public
as $$
begin
  if auth.role() <> 'service_role' then
    raise exception 'service_role required';
  end if;

  return query
  select
    c.table_name::text,
    c.column_name::text,
    c.udt_name::text,
    c.is_nullable::text,
    c.column_default::text,
    c.ordinal_position::integer
  from information_schema.columns c
  where c.table_schema = coalesce(nullif(trim(p_schema), ''), 'uk_aq_core')
    and (p_table_names is null or c.table_name = any(p_table_names))
  order by c.table_name, c.ordinal_position;
end;
$$;

revoke all on function uk_aq_public.uk_aq_rpc_info_schema_columns(text, text[]) from public;
grant execute on function uk_aq_public.uk_aq_rpc_info_schema_columns(text, text[]) to service_role;

create or replace function uk_aq_public.uk_aq_rpc_info_schema_primary_keys(
  p_schema text default 'uk_aq_core',
  p_table_names text[] default null
)
returns table (
  table_name text,
  constraint_name text,
  column_name text,
  ordinal_position integer
)
language plpgsql
security definer
set search_path = pg_catalog, public
as $$
begin
  if auth.role() <> 'service_role' then
    raise exception 'service_role required';
  end if;

  return query
  select
    kcu.table_name::text,
    kcu.constraint_name::text,
    kcu.column_name::text,
    kcu.ordinal_position::integer
  from information_schema.table_constraints tc
  join information_schema.key_column_usage kcu
    on tc.constraint_name = kcu.constraint_name
   and tc.table_schema = kcu.table_schema
   and tc.table_name = kcu.table_name
  where tc.table_schema = coalesce(nullif(trim(p_schema), ''), 'uk_aq_core')
    and tc.constraint_type = 'PRIMARY KEY'
    and (p_table_names is null or tc.table_name = any(p_table_names))
  order by kcu.table_name, kcu.constraint_name, kcu.ordinal_position;
end;
$$;

revoke all on function uk_aq_public.uk_aq_rpc_info_schema_primary_keys(text, text[]) from public;
grant execute on function uk_aq_public.uk_aq_rpc_info_schema_primary_keys(text, text[]) to service_role;
