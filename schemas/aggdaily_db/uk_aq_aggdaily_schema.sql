-- Phenomena (mirrored)
create table if not exists phenomena (
  id bigint generated by default as identity primary key,
  label text not null,
  source_label text,
  notation text,
  pollutant_label text,
  observed_property_id bigint references observed_properties(id),
  connector_id integer not null references connectors(id) on delete cascade
);

do $$
begin
  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'uk_aq_core'
      and table_name = 'phenomena'
      and column_name = 'eionet_uri'
  ) and not exists (
    select 1
    from information_schema.columns
    where table_schema = 'uk_aq_core'
      and table_name = 'phenomena'
      and column_name = 'source_label'
  ) then
    alter table uk_aq_core.phenomena
      rename column eionet_uri to source_label;
  end if;
end
$$;

alter table if exists phenomena
